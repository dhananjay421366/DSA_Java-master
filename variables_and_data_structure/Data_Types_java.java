public class Data_Types_java {
    public static void main(String args[]) {
        // Primitive Data Types
        // byte, short , char , boolean , int , float , long , double
        int a = 10; // size- 4 byte [-2 billon to 2 billon]
        System.out.println(a);

        short b = 20; // size 2 byte
        System.out.println(b);

        char c = 'c'; // store only a single character || size - 2 byte
        System.out.println(c);

        boolean d = true; // either true or false || size- 1 byte
        System.out.println(d);

        byte e = 30; // size - 1 byte , [-128 to 127]
        System.out.println(e);

        float f = 3; // size-4 byte
        System.out.println(f);

        long g = 10000000000L; // size-8 byte
        System.out.println(g);

        double h = 1234321; // size-8 byte
        System.out.println(h);

        // No Primitive data Types
        // String , Array , class , object , interface
    }
}
