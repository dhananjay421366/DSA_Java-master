public class JavaOperator {
    public static void main(String args[]) {

        // types of operator
        // 1. Arithmetic Operators
        // 2. Relational Operators
        // 3. Logical Operator
        // 4. Bitwise Operator
        // 5. Assignment Operator

        // 1. Arithmetic Operator
        // +, -,*,/,%

        // unary operator
        // ++, --
        int A = 10;
        int B = 12;
        int sum = A + B;
        System.out.println("The sum operator return sum of numbers:" + sum);

        // Relational operator
        // == , != , >, < , >= , <=
        if (A < B) {
            System.out.println("A is less then B");
        }

        // Logical Operators
        // && , || , !
        System.out.println();

        // Assignment Operator
        // = , += , -= , *= , /=

    }

}